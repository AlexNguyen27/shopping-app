class User {
  constructor(id, firstName, lastName, phone, email, address, profileUrl, description) {
    this.id = id;
    this.firstName = firstName;
    this.lastName = lastName;
    this.phone = phone;
    this.email = email;
    this.address = address;
    this.profileUrl = profileUrl;
    this.description = description;
  }
}

export default User;
